export function applyFieldMotion(baseX,baseY,baseZ,ph,t,s,state,arrival){
const active=Math.min(1,s.tracking*0.7+s.friction*0.45+s.mixed_weather*0.5+s.reach*0.25+s.arrival*0.35);
const ambientX=Math.sin(t*0.19+ph*0.7)*0.08+Math.cos(t*0.13+baseY*0.45)*0.05;
const ambientY=Math.cos(t*0.21+ph*0.9)*0.08+Math.sin(t*0.1+baseX*0.52)*0.04;
const driftCenterX=Math.sin(t*(0.065+s.drift*0.09)+state.confidence*2.2)*s.drift*0.95+Math.sin(t*0.17+ph*0.4)*s.mixed_weather*0.25;
const driftCenterY=Math.cos(t*(0.058+s.drift*0.08)+s.nowness*1.8)*s.drift*0.9+Math.cos(t*0.145+ph*0.43)*s.mixed_weather*0.22;
const dx=baseX-driftCenterX,dy=baseY-driftCenterY,r=Math.max(0.1,Math.hypot(dx,dy));
const spiral=(-dy/r)*(0.09+s.tracking*0.58+s.mixed_weather*0.24);
const alignA=Math.sin(t*(0.55+s.tracking*2.2)+baseX*1.5+ph);
const alignB=Math.cos(t*(0.47+s.tracking*1.9)+baseY*1.4-ph*0.3);
const align=(alignA+alignB)*0.08*(s.tracking+s.mixed_weather*0.75+s.aliveness*0.2);
const flockWave=Math.sin(t*(0.8+s.tracking*1.4)+r*1.6+ph*0.4)*(0.03+s.tracking*0.2+s.mixed_weather*0.08);
const reachProbe=(0.16+s.reach*0.84)*(0.55+0.45*Math.sin(t*0.8+ph*1.6));
const reachCurl=Math.sin(t*1.18+ph*1.1+r*0.9)*0.22*s.reach;
const asymProbe=Math.sin(t*0.63+baseX*0.9-ph*0.55)*s.reach*0.22;
const shear=Math.sin((baseY*2.4+t*1.75)+Math.sin(baseX*1.3+t*0.5))*s.friction*0.4;
const eddy=Math.cos((baseX*2.1-baseY*1.7)+t*(1.2+s.friction))*s.friction*0.32;
const turbulence=Math.sin(baseX*3.2-baseY*2.7+t*(1.7+s.friction*2.1))*s.friction*(0.08+0.16*s.mixed_weather);
const foldEcho=Math.sin((baseX+baseY)*(0.7+s.folding*1.2)+t*0.34+Math.sin(t*0.09)*3.0)*s.folding*0.24;
const ghostEcho=Math.sin((baseX*0.7-baseY*0.75)*(0.8+s.folding)+t*0.27+Math.sin(ph+t*0.12))*s.folding*0.2;
const cluster=Math.sin(r*(1.8+s.tracking*0.8)-t*(0.38+s.tracking*0.44)+ph)*(s.tracking*0.22+s.mixed_weather*0.12);
const placeHold=(-baseX*0.015-baseY*0.01)*(0.5+s.place*0.8);
const arrivalPull=arrival.pull||0;
const arrivalBloom=arrival.bloom||0;
const x=baseX+ambientX+spiral+align+flockWave+dx*(reachProbe*0.095)+reachCurl+asymProbe+shear+turbulence+cluster+placeHold-baseX*arrivalPull*0.22;
const y=baseY+ambientY+align*0.92+foldEcho+ghostEcho+(-dx/r)*(s.reach*0.22)+eddy+dy*(s.drift*0.08)-cluster*0.48-baseY*arrivalPull*0.22;
const z=baseZ+Math.sin(t*0.27+ph*1.2)*(0.14+s.inhabited_silence*0.14)+Math.cos(t*0.43+ph+dx*0.5)*(0.08+s.aliveness*0.2)+arrivalBloom*0.34*(0.5+Math.sin(ph*2.0)*0.5)+active*0.12*Math.sin(t*0.9+ph+r);
return [x,y,z];
}
